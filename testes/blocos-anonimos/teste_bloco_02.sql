PROMPT Evidencia apos sucesso:
SELECT c.ID_CONTRIBUICAO, c.STT_CONTRIBUICAO, h.STATUS_NAVE_HANGAR, h.NM_GRAVADO_HANGAR
  FROM TB_CONTRIBUICAO c
  LEFT JOIN TB_HANGAR h ON h.TB_CONTRIB_ID_CONTRIB = c.ID_CONTRIBUICAO
 WHERE c.ID_CONTRIBUICAO = 'CONTRIB-001';

