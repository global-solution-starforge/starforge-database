SET SERVEROUTPUT ON;

PROMPT Cenario de sucesso: preparando contribuicao pendente de comandante.
UPDATE TB_CONTRIBUICAO
   SET STT_CONTRIBUICAO = 'PENDENTE',
       TB_TIER_ID_TIER = 'TIER-CMD',
       VL_CONTRIBUICAO = 600
 WHERE ID_CONTRIBUICAO = 'CONTRIB-001';
COMMIT;

SELECT ID_CONTRIBUICAO, STT_CONTRIBUICAO, VL_CONTRIBUICAO
  FROM TB_CONTRIBUICAO
 WHERE ID_CONTRIBUICAO = 'CONTRIB-001';

